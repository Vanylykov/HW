try:
    score = int(input("Введіть кількість отриманих балів: "))
    if 0 <= score <= 49:
        print("Оцінка: незадовільно")
    elif 50 <= score <= 69:
        print("Оцінка: задовільно")
    elif 70 <= score <= 89:
        print("Оцінка: добре")
    elif 90 <= score <= 100:
        print("Оцінка: відмінно")
    else:
        print("Невірний ввід балів. Будь ласка, введіть число від 0 до 100.")
except ValueError:
    print("Будь ласка, введіть ціле число.")